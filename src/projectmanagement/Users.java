    public void listUpdate(){
        LoginForm LF = new LoginForm();
        DefaultListModel userList = new DefaultListModel();
        for (int i = 0; i < LF.c.employees.size(); i++){
            userList.addElement(LF.c.employees.get(i).getId()+"");
        }
        if(LF.c.currentUserRole == 4){
            for (int i = 0; i < LF.c.teamLeaders.size(); i++){
                userList.addElement(LF.c.teamLeaders.get(i).getId()+"");
            }
            
            for (int i = 0; i < LF.c.projectManagers.size(); i++){
                userList.addElement(LF.c.projectManagers.get(i).getId()+"");
            }
            
            for (int i = 0; i < LF.c.admins.size(); i++){
                userList.addElement(LF.c.admins.get(i).getId()+"");
            }
        }
        Error.setText("");
        UsersList.setModel(userList);
    }
    
    public void writeUser(int id){
        LoginForm LF = new LoginForm();
        String firstName = "";
        String lastName = "";
        String pass = "";
        String mail = "";
        String Addr = "";
        String Pho = "";
        String CT = "";
        String PT = "";
        String WH = "";
        if(LF.c.getEmployeeById(id).getId() != 0){
            firstName = LF.c.getEmployeeById(id).getFirstName();
            lastName = LF.c.getEmployeeById(id).getLastName();
            pass = LF.c.getEmployeeById(id).getPassword();
            mail = LF.c.getEmployeeById(id).getEmail();
            Addr = LF.c.getEmployeeById(id).getAddress();
            Pho = LF.c.getEmployeeById(id).getphoneNumber();
            CT = LF.c.getEmployeeById(id).getFinishedTasks().size() + "";
            PT = LF.c.getEmployeeById(id).getPendingTasks().size() + "";
            WH = LF.c.getEmployeeById(id).getMyHours() + "";
            System.out.println(CT);
        }
        else if(LF.c.getTeamLeaderById(id).getId() != 0){
            firstName = LF.c.getTeamLeaderById(id).getFirstName();
            lastName = LF.c.getTeamLeaderById(id).getLastName();
            pass = LF.c.getTeamLeaderById(id).getPassword();
            mail = LF.c.getTeamLeaderById(id).getEmail();
            Addr = LF.c.getTeamLeaderById(id).getAddress();
            Pho = LF.c.getTeamLeaderById(id).getphoneNumber();
            CT = "";
            PT = "";
            WH = "";
        }
        else if(LF.c.getProjectManagerById(id).getId() != 0){
            firstName = LF.c.getProjectManagerById(id).getFirstName();
            lastName = LF.c.getProjectManagerById(id).getLastName();
            pass = LF.c.getProjectManagerById(id).getPassword();
            mail = LF.c.getProjectManagerById(id).getEmail();
            Addr = LF.c.getProjectManagerById(id).getAddress();
            Pho = LF.c.getProjectManagerById(id).getphoneNumber();
            CT = "";
            PT = "";
            WH = "";
        }
        else if(LF.c.getAdminById(id).getId() != 0){
            firstName = LF.c.getAdminById(id).getFirstName();
            lastName = LF.c.getAdminById(id).getLastName();
            pass = LF.c.getAdminById(id).getPassword();
            mail = LF.c.getAdminById(id).getEmail();
            Addr = LF.c.getAdminById(id).getAddress();
            Pho = LF.c.getAdminById(id).getphoneNumber();
            CT = "";
            PT = "";
            WH = "";
        }
            FN.setText(firstName);
            LN.setText(lastName);
            PW.setText(pass);
            Email.setText(mail);
            Address.setText(Addr);
            Phone.setText(Pho);
            CTasks.setText(CT);
            PTasks.setText(PT);
            WHours.setText(WH);
    }
    
