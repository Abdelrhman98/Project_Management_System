    private void SearchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SearchButtonMouseClicked
        LoginForm LF = new LoginForm();
        int id;
        if(!(Search.getText().isEmpty())){
            id = Integer.parseInt(Search.getText());
        }
        else id = 0;
        if(id > 0 && LF.c.getEmployeeById(id).getId() != 0){
            DefaultListModel userList = new DefaultListModel();
            userList.addElement(LF.c.getEmployeeById(id).getId()+"");
            UsersList.setModel(userList);
            Error.setText("");
        }
        else if(id > 0 && LF.c.getEmployeeById(id).getId() == 0){
            Error.setText("User not found!");
        }
        else{
            listUpdate();
            Error.setText("");
        }
    }//GEN-LAST:event_SearchButtonMouseClicked

