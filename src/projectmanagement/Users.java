    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
            LoginForm LF = new LoginForm();
            int projectId = Integer.parseInt(pId.getText());
            int employeeId = Integer.parseInt(UsersList.getSelectedValue());
            try{
                ArrayList<String> t = LF.c.addNewTask(TN.getText(), projectId, LF.c.getEmployeeById(employeeId), TD.getText(), Deadline.getText());
                if(t.size()>0){
                    for(int i=0;i<t.size();i++){
                        tError.setText(t.get(i));
                    }
                    
                }
                else{
                        tError.setText("");
                        writeUser(employeeId);
                }
            }
            catch(Exception e){
                tError.setText("Task not added!");
            }
            
    }//GEN-LAST:event_jButton3MouseClicked

